    public void procuraCliente(String pesquisa, String tipo, DefaultTableModel modelo) {
        String SQLSelection;

        switch (tipo) {
            case "nome":
                SQLSelection = "select * from cliente where cli_nome like '%" + pesquisa + "%';";
                break;
            case "telefone":
                SQLSelection = "select * from cliente where cli_telefone like '%" + pesquisa + "%';";
                break;
            default:
                SQLSelection = "select * from cliente where cli_rua like '%" + pesquisa + "%';";
                break;
        }
        try (PreparedStatement pstm = ConectaBanco.getConnection().prepareStatement(SQLSelection)) {

            ResultSet rs = pstm.executeQuery();
//            if (pesquisa.equals("")) {
//                rs.next();
//            }
            while (rs.next()) {
                ClienteBean clienteBeans2 = new ClienteBean();
                clienteBeans2.setObservacao(rs.getString("cli_obs"));
                clienteBeans2.setCep(rs.getString("cli_cep"));
                clienteBeans2.setAniversario(VerificarData.converteParaJAVA(rs.getString("cli_aniversario")));
                clienteBeans2.setTelCelular(rs.getString("cli_tel_cel"));
                clienteBeans2.setEmail(rs.getString("cli_email"));
                clienteBeans2.setNumero(rs.getInt("cli_nro_ender"));
                clienteBeans2.setCodigoCliente(rs.getInt("cli_cod"));
                clienteBeans2.setNome(rs.getString("cli_nome"));
                clienteBeans2.setRua(rs.getString("cli_rua"));
                clienteBeans2.setBairro(rs.getString("cli_bairro"));
                clienteBeans2.setTelefone(rs.getString("cli_telefone"));
                clienteBeans2.setDataCadastro(VerificarData.converteParaJAVA(rs.getString("cli_datacad")));
                clienteBeans2.setCidade(rs.getString("cli_cidade"));
                modelo.addRow(new Object[]{clienteBeans2, rs.getString("cli_rua"), rs.getString("cli_bairro"), rs.getString("cli_telefone")});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Imposs√≠vel Cadastrar", "Erro de SQL", 0, new ImageIcon("imagens/cancelar.png"));
        }
    }

